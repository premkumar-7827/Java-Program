package org.example;

import java.sql.SQLOutput;

public class Variable {
    public static void main(String[] args) {
        //Declaring Variable and its Value
        boolean a = true;
        byte b = 1;
        char c = 'q';
        short d = 32;
        int e = 12;
        long f = 1234;
        float g = 10.11F;
        double h = 10.212121;
        //Printing the variable & its value
        System.out.println("This is boolean - " + a);
        System.out.println("This is byte - " + b);
        System.out.println("This is char - " + c);
        System.out.println("This is short - " + d);
        System.out.println("This is int - " + e);
        System.out.println("This is long - " + f);
        System.out.println("This is float - " + g);
        System.out.println("This is double - " + h);


    }
}
